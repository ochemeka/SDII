                                     <p>SPONTANEOUS DEVELOPMENT INTERNATIONAL INITIATIVE (SDII) started with a concern and an idea. The concern: How to counter negative perceptions of Africa and help foster and promote grassroots development. The idea: Help small, effective nonprofit organizations working to overcome poverty in Africa by shining a light on their work.
<br />
SPONTANEOUS DEVELOPMENT INTERNATIONAL INITIATIVE (SDII) is a non-governmental organisation which is aimed at aspiring people
</p>
The challenges facing the people of Sub Saharan Africa are complex and interrelated. Thus we set out to ensure that our membership included organizations that addressed the full range of development issues,  from education, health, agriculture, the environment etc. We also included organizations undertaking scientific research on a range of issues related to food, agriculture, insect science, livestock, and more. Without strong science advances in food production, medical treatment, pest control, and more, development cannot be made and sustained.
<p>
We believed that by bringing them together in an alliance, these organizations would learn about each other, form partnerships, and make the whole greater than the sum of its parts. Through Aid-for- Africa-sponsored events and on line, member organizations have come to do just that. They identify common problems, share best practices, and develop innovative solutions.
